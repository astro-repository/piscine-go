package main

import "os"
import "fmt"

func main() {
	Decompte := len(os.Args)

	for 0<Decompte {
		for i := 0; i < len(os.Args)-1; i++ {
			if os.Args[i][0] > os.Args[i+1][0]{
				intermediaire := os.Args[i]
				os.Args[i] = os.Args[i+1]
				os.Args[i+1] = intermediaire
			}
		}
		Decompte--
	}
	for _, v := range os.Args {
		fmt.Println(v)
	}
}